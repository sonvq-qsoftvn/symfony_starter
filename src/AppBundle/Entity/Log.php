<?php

namespace AppBundle\Entity;

/**
 * Log
 */
class Log
{
    /**
     * @var \DateTime
     */
    private $data = '0000-00-00 00:00:00';

    /**
     * @var string
     */
    private $classe = '';

    /**
     * @var string
     */
    private $messaggio;

    /**
     * @var string
     */
    private $path = '';

    /**
     * @var string
     */
    private $addr = '';

    /**
     * @var integer
     */
    private $id;


    /**
     * Set data
     *
     * @param \DateTime $data
     *
     * @return Log
     */
    public function setData($data)
    {
        $this->data = $data;

        return $this;
    }

    /**
     * Get data
     *
     * @return \DateTime
     */
    public function getData()
    {
        return $this->data;
    }

    /**
     * Set classe
     *
     * @param string $classe
     *
     * @return Log
     */
    public function setClasse($classe)
    {
        $this->classe = $classe;

        return $this;
    }

    /**
     * Get classe
     *
     * @return string
     */
    public function getClasse()
    {
        return $this->classe;
    }

    /**
     * Set messaggio
     *
     * @param string $messaggio
     *
     * @return Log
     */
    public function setMessaggio($messaggio)
    {
        $this->messaggio = $messaggio;

        return $this;
    }

    /**
     * Get messaggio
     *
     * @return string
     */
    public function getMessaggio()
    {
        return $this->messaggio;
    }

    /**
     * Set path
     *
     * @param string $path
     *
     * @return Log
     */
    public function setPath($path)
    {
        $this->path = $path;

        return $this;
    }

    /**
     * Get path
     *
     * @return string
     */
    public function getPath()
    {
        return $this->path;
    }

    /**
     * Set addr
     *
     * @param string $addr
     *
     * @return Log
     */
    public function setAddr($addr)
    {
        $this->addr = $addr;

        return $this;
    }

    /**
     * Get addr
     *
     * @return string
     */
    public function getAddr()
    {
        return $this->addr;
    }

    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }
}
