<?php

namespace AppBundle\Entity;

/**
 * RequestAssignmentReason
 */
class RequestAssignmentReason
{
    /**
     * @var string
     */
    private $assignmentCode;

    /**
     * @var string
     */
    private $assignmentDescr;

    /**
     * @var integer
     */
    private $assignmentReason;


    /**
     * Set assignmentCode
     *
     * @param string $assignmentCode
     *
     * @return RequestAssignmentReason
     */
    public function setAssignmentCode($assignmentCode)
    {
        $this->assignmentCode = $assignmentCode;

        return $this;
    }

    /**
     * Get assignmentCode
     *
     * @return string
     */
    public function getAssignmentCode()
    {
        return $this->assignmentCode;
    }

    /**
     * Set assignmentDescr
     *
     * @param string $assignmentDescr
     *
     * @return RequestAssignmentReason
     */
    public function setAssignmentDescr($assignmentDescr)
    {
        $this->assignmentDescr = $assignmentDescr;

        return $this;
    }

    /**
     * Get assignmentDescr
     *
     * @return string
     */
    public function getAssignmentDescr()
    {
        return $this->assignmentDescr;
    }

    /**
     * Get assignmentReason
     *
     * @return integer
     */
    public function getAssignmentReason()
    {
        return $this->assignmentReason;
    }
}
