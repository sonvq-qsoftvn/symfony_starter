<?php

namespace AppBundle\Entity;

/**
 * Gopl
 */
class Gopl
{
    /**
     * @var integer
     */
    private $rotazioni = '0';

    /**
     * @var \DateTime
     */
    private $data;


    /**
     * Set rotazioni
     *
     * @param integer $rotazioni
     *
     * @return Gopl
     */
    public function setRotazioni($rotazioni)
    {
        $this->rotazioni = $rotazioni;

        return $this;
    }

    /**
     * Get rotazioni
     *
     * @return integer
     */
    public function getRotazioni()
    {
        return $this->rotazioni;
    }

    /**
     * Get data
     *
     * @return \DateTime
     */
    public function getData()
    {
        return $this->data;
    }
}
